/// RGB colour value.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct Rgb(pub u8, pub u8, pub u8);

/// 16-slot theme palette per THEMING.md.
#[derive(Debug, Clone)]
pub struct ThemePalette {
    pub name: &'static str,
    // Surface colours
    pub foreground: Rgb,
    pub background: Rgb,
    pub modeline: Rgb,
    pub highlight: Rgb,
    // Semantic roles
    pub critical: Rgb,
    pub popout: Rgb,
    pub strong: Rgb,
    pub salient: Rgb,
    pub faded: Rgb,
    pub subtle: Rgb,
    // Mid-tones
    pub mild: Rgb,
    pub ultralight: Rgb,
    // Accent colours
    pub accent_red: Rgb,
    pub accent_green: Rgb,
    pub accent_blue: Rgb,
    pub accent_yellow: Rgb,
}

